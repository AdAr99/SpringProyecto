CREATE TABLE `dbviajes`.`hotel` (
                                    `idhotel` INT NOT NULL AUTO_INCREMENT,
                                    `nombre` VARCHAR(45) NULL,
                                    `categoria` VARCHAR(45) NULL,
                                    `precio` VARCHAR(45) NULL,
                                    `disponible` VARCHAR(45) NULL,
                                    PRIMARY KEY (`idhotel`))
    ENGINE = InnoDB
DEFAULT CHARACTER SET = utf8;
